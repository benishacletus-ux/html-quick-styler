class ColorPaletteGenerator:
    @staticmethod
    def get_palette(theme: str) -> dict:
        palettes = {
            'Corporate Slate': {
                'primary': '#0F172A', 'bg': '#F8FAFC',
                'surface': '#FFFFFF', 'text': '#1E293B', 'border': '#E2E8F0'
            },
            'Cyber Dark': {
                'primary': '#00F2FF', 'bg': '#050505',
                'surface': '#0D0D0D', 'text': '#E0E0E0', 'border': '#1F1F1F'
            },
            'Nordic Forest': {
                'primary': '#2D3A3A', 'bg': '#F4F1EA',
                'surface': '#FFFFFF', 'text': '#2D3A3A', 'border': '#D1CDBE'
            },
            'Royal Gold': {
                'primary': '#1A1A1A', 'bg': '#FFFFFF',
                'surface': '#FAFAFA', 'text': '#1A1A1A', 'border': '#E5E5E5'
            }
        }
        return palettes.get(theme, palettes['Corporate Slate'])

