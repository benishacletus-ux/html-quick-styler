def update_preview(name, desc, theme):
    code = HTMLGenerator.generate(name, desc, theme)
    return code, code

with gr.Blocks(css=custom_ui_css, title="Pro Styler") as demo:
    gr.Markdown("# 🌸 Professional Build Generator")
    with gr.Row():
        with gr.Column(scale=1):
            name = gr.Textbox(label="Brand/Project Name", value="Aether Studio")
            desc = gr.Textbox(label="Vision Statement", value="The intersection of elite engineering and minimalist aesthetics.")
            theme = gr.Radio(
                ["Corporate Slate", "Cyber Dark", "Nordic Forest", "Royal Gold"],
                label="Environment Select",
                value="Corporate Slate"
            )
            # We add the elem_id here so the CSS above knows which button to style
            generate_btn = gr.Button("Generate Build", variant="primary", elem_id="generate_btn")

        with gr.Column(scale=2):
            code_out = gr.Code(label="Source Code", language="html")
            preview = gr.HTML()

    generate_btn.click(update_preview, [name, desc, theme], [code_out, preview])

if __name__ == "__main__":
    demo.launch()